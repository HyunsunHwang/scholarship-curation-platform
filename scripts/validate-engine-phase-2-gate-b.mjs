import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const fixture = JSON.parse(fs.readFileSync(path.join(root, ".tmp/engine-phase-2-gate-b/fixture-results.json"), "utf8"));
const live = JSON.parse(fs.readFileSync(path.join(root, ".tmp/engine-phase-2-gate-b/live-summary.json"), "utf8"));
const report = JSON.parse(fs.readFileSync(path.join(root, "reports/engine-phase-2-gate-b-baseline.json"), "utf8"));
const checkpointSource = fs.readFileSync(path.join(root, "lib/crawler-engine/checkpoint.mjs"), "utf8");
const commonRunnerSource = fs.readFileSync(path.join(root, "lib/crawler-engine/common-runner.mjs"), "utf8");
const cliSource = fs.readFileSync(path.join(root, "scripts/crawl-scholarship-notices.mjs"), "utf8");
const serializedReport = JSON.stringify(report);
const checks = [];
const check = (name, condition) => {
  checks.push({ name, passed: Boolean(condition) });
  console.log(`${condition ? "PASS" : "FAIL"} ${name}`);
};
const scenario = (name) => fixture.scenarios?.[name] ?? {};
const validationPassed = (name) => fixture.validations?.some((row) => row.name === name && row.passed === true);

const schema = scenario("checkpoint schema is versioned and deterministic");
const explicitResume = scenario("resume is explicit opt-in and existing checkpoint is protected");
const atomic = scenario("atomic write uses temp rename and preserves existing checkpoint on failure");
const fingerprint = scenario("configuration and source fingerprints are deterministic and scoped");
const failClosed = scenario("corrupt and incompatible checkpoints fail closed before scheduling");
const sourceSkip = scenario("completed source is skipped before scheduling");
const itemSkip = scenario("completed work item is skipped before detail and parser scheduling");
const recovery = scenario("external cancellation saves progress and resumed run is equivalent");
const settle = scenario("bounded settle returns abandoned evidence without unlimited wait");
const lateCallback = scenario("cancelled checkpoint rejects late completion callbacks");
const settleTimer = scenario("bounded settle clears losing grace timers");
const delay = scenario("retry and rate-limit delays remain cancellable");
const signals = scenario("SIGINT and SIGTERM handlers are deterministic and cleaned up");
const cache = scenario("Phase 3 parser cache stays separate and reusable");
const identityEqual = JSON.stringify(recovery.uninterrupted_identity_set) === JSON.stringify(recovery.resumed_identity_set);
const status = execFileSync("git", ["-c", `safe.directory=${root.replaceAll("\\", "/")}`, "status", "--porcelain"], { cwd: root, encoding: "utf8" });
const changedPaths = status.split(/\r?\n/).filter(Boolean).map((line) => line.slice(3).replaceAll("\\", "/"));

check("official Gate B identity", report.official_gate === "Engine Phase 2 Completion — Gate B");
check("computed fixture suite passes", fixture.test_count >= 15 && fixture.failed_count === 0 && fixture.passed_count === fixture.test_count);
check("versioned checkpoint schema", schema.schema_version === 1 && checkpointSource.includes("CRAWLER_CHECKPOINT_SCHEMA_VERSION = 1"));
check("deterministic checkpoint serialization", validationPassed("checkpoint schema is versioned and deterministic") && schema.deterministic === true);
check("atomic temp write and rename implementation", atomic.temp_write_then_rename === true && checkpointSource.includes("fsApi.open(tempPath, \"wx\")") && checkpointSource.includes("fsApi.rename(tempPath, resolvedPath)"));
check("existing checkpoint preserved on injected failure", atomic.existing_preserved === true && atomic.temp_file_count === 0);
check("configuration fingerprint scoped", fingerprint.secret_excluded === true && fingerprint.meaningful_change_detected === true);
check("source-set fingerprint order independent", fingerprint.source_order_independent === true);
check("corrupt checkpoint fail closed", failClosed.corrupt_code === "checkpoint_corrupt_json" && failClosed.scheduled_count === 0);
check("incompatible checkpoint fail closed", failClosed.incompatible_code === "checkpoint_runner_mismatch" && failClosed.scheduled_count === 0);
check("explicit resume CLI and checkpoint protection", explicitResume.resume_explicit === true && explicitResume.existing_checkpoint_protected === true && checkpointSource.includes("argument === \"--resume\""));
check("completed source scheduling skip", sourceSkip.reexecuted_completed_source_count === 0 && commonRunnerSource.includes("shouldSkipSource"));
check("completed work item scheduling skip", itemSkip.reexecuted_completed_work_item_count === 0 && itemSkip.parser_calls_for_completed_item === 0 && commonRunnerSource.includes("completedWorkItemKeys"));
check("resume identity sets independently equal", identityEqual && new Set(recovery.resumed_identity_set).size === recovery.resumed_identity_set.length);
check("resume duplicate identity zero", recovery.resume_duplicate_identity_count === 0);
check("second resume creates zero new results", recovery.resume_second_execution_new_result_count === 0);
check("external cancellation explicit", recovery.interrupted_status === "cancelled" && recovery.checkpoint_saved === true);
check("SIGINT integration and cleanup", signals.sigint_verified === true && cliSource.includes("installCrawlerSignalHandlers"));
check("SIGTERM integration and cleanup", signals.sigterm_verified === true && signals.dangling_signal_listener_count === 0);
check("retry delay cancellation", delay.retry_timer_cleared >= 1);
check("rate-limit wait cancellation", delay.rate_limit_cancelled_wait_count >= 1);
check("bounded settle no unlimited wait", settle.abandoned_count === 1 && settle.unlimited_wait === false && settle.elapsed_ms < 500);
check("late completion cannot mutate cancelled checkpoint", lateCallback.late_completed_source_count === 0 && lateCallback.late_completed_work_item_count === 0);
check("bounded settle grace timer cleanup", settleTimer.dangling_settle_timer_count === 0);
check("Phase 3 cache reused", cache.cache_hit_on_resume === true);
check("checkpoint excludes full document text", cache.checkpoint_contains_full_document_text === false && !checkpointSource.includes("normalized_text:"));
check("checkpoint excludes raw document bytes", cache.checkpoint_contains_raw_document_bytes === false && !checkpointSource.includes("raw_bytes:"));
check("tracked report excludes absolute local paths", !/[A-Za-z]:[\\/]/.test(serializedReport) && !serializedReport.includes(root));
check("live bounded source count", live.bounds?.source_count > 0 && live.bounds.source_count <= 2 && live.bounds.notice_limit_per_source === 1);
check("live common runner and Gate A limiter", live.runtime_path?.common_runner_used === true && live.runtime_path?.gate_a_limiter_reused === true);
check("live cancellation checkpoint", live.invariants?.interrupted_cancelled === true && live.invariants?.cancellation_checkpoint_saved === true);
check("live explicit resume skips completion", live.runtime_path?.explicit_resume_used === true && live.invariants?.resumed_skipped_source_count >= 1);
check("live duplicate identity zero", live.invariants?.duplicate_identity_count === 0);
check("database access zero", report.safety?.database_accessed === false && report.safety?.database_written === false);
check("production access zero", report.safety?.production_accessed === false);
check("external LLM zero", report.safety?.external_llm_called === false);
check("migration zero", report.safety?.migration_created_or_executed === false);
check("no scheduler queue or worker", report.safety?.production_scheduler_added === false && report.safety?.queue_or_worker_added === false);
check("no full source run", report.safety?.full_613_source_run === false);
check("no migrations admin API cron queue worker changed", !changedPaths.some((name) => /^(supabase\/|app\/api\/|app\/admin\/|.*(?:cron|queue|worker).*)/i.test(name)));
check("no parallel runner or identity", report.architecture?.parallel_runner_added === false && report.architecture?.second_source_identity_added === false && report.architecture?.second_normalized_graph_added === false);
check("report result recomputed", report.result === (checks.every((row) => row.passed) ? "PASS" : "FAIL"));

const failed = checks.filter((row) => !row.passed);
console.log(`ENGINE PHASE 2 GATE B: ${failed.length === 0 ? "PASS" : "FAIL"}`);
console.log(`checks=${checks.length - failed.length}/${checks.length}`);
if (failed.length > 0) process.exitCode = 1;
